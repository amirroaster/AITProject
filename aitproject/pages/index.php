			<h1>San Francisco Seismic Hazard Zones</h1>
		</aside>
		<section>
			<p>
			This is a digital Seismic Hazard Zone Map presenting areas where liquefaction and landslides may occur during a strong earthquake. Three types of geological hazards, referred to as seismic hazard zones, may be featured on the map
			</p>
			<details>
			1) liquefaction, 2) earthquake-induced landslides, and 3) overlapping liquefaction and earthquake-induced landslides. Developers of properties falling within any of the three zones may be required to investigate the potential hazard and mitigate its threat during the local permitting process.
			</details>
